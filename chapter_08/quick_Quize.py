# Write a program to greet a user with “Good day” using functions.

def greet_user(name,ending):
    print(f"Good day!, {name}")
    print(ending)


greet_user('mingo','thank you!')  #here, mingo and thank you! are  argument
greet_user('alice','thank you!')  #here, alice and thank you!are argument
greet_user('john','thank you!')   #here, john and thank you!are  argument